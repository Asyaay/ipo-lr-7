import json #импортируем библиотеку
with open(r'flowers.json', 'r+', encoding='UTF-8') as file: #открываем файл
    flowers = json.load(file) #загружаем файл
list_of_choises={ 
    1:"Вывести все записи", 
    2:"Вывести запись по полю", 
    3:"Добавить запись", 
    4:"Удалить запись по полю",
    5:"Выйти из программы"
    } #создаем словарь 
for i in list_of_choises: #перебираем значения из словаря
    print(i,':',list_of_choises[i]) #выводим
z = 0 #создаем счетчик для хранения значений
circle = 0 #создаем счетчик
while(z != 5): #пока цикл работает
    choise = int(input('Выберите пункт: ')) #просим ввести 
