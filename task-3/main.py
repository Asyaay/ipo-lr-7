    print('--------------') #выводим
    if choise == 1: #если выбор это 1, то
        for z in flowers: #перебираем значения 
            for i in z: #перебираем значения
                print(i,':',z[i]) #выводим
            print('--------------') #выводим
        circle += 1 #обновляем счетчик
    if choise == 2: #если выбор это 2, то
        for z in flowers: #перебираем значения
            for i in z: #перебираем значения
                if i == 'id': #если есть такое значение, то
                    if z['id'] != 'Удалено': #если значение не равно, то
                        print(i,':',z[i]) #выводим
                        list1 = list(z.keys()) #присваиваем список
                        print('Позиция в словаре: ',list1.index(i)) #выводим
                    if z['id'] == 'Удалено': #если значение равно, то
                        print('Не найдено') #выводим
            print('--------------') #выводим
        circle += 1 #обновляем счетчик
    if choise == 3: #если выбор это 3, то
        info_key = input('Введите поле, которое хотите добавить: ') #просим ввести 
        info = input('Введите информацию в Ваше поле: ') #просим ввести 
        for z in flowers: #перебираем значения
            z[info_key] = info #добавляем новое значение
            print('--------------') #выводим
            break #останавливаем цикл
        circle += 1 #обновляем счетчик
    if choise == 4: #если выбор это 4, то
        for z in flowers: #перебираем значения
            for i in z: #перебираем значения
                if i == 'id': #если есть такое значение, то
                    if z['id'] != 'Удалено': #если значение не равно, то
                        z['id'] = 'Удалено' #удаляем
                        break #останавливаем цикл
